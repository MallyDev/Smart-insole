void ColorLed() {
	uint16_t c1 = 1000, c2 = 1000, c3 = 1000;


	if(color[0] == 1){
		if(color[1] == 1 && color[4] == 1 ){
			//green
			c1 = 0;
			c3 = 0;
			__HAL_TIM_SET_COMPARE(&htim11, TIM_CHANNEL_1, 0);
		}else{
			//red
			c2 = 0;
			c3 = 0;
			__HAL_TIM_SET_COMPARE(&htim11, TIM_CHANNEL_1, 1000);
		}
	}else{
			if(color[2] == 1){
				//blue
				c1 = 0;
				c2 = 0;
				__HAL_TIM_SET_COMPARE(&htim11, TIM_CHANNEL_1, 0);
			}else if(color[0]==0 && color[1]==0 && color[2]==0){
				//yellow
				c3 = 0;
				__HAL_TIM_SET_COMPARE(&htim11, TIM_CHANNEL_1, 1000);
			}
		}


	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, c1);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, c2);
	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, c3);
}

int unbouncingButton() {
	int PrevState = 1;
	int CurrentState = 1;
	int c = 0;

	PrevState = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);

	for (c = 0; c < DEBOUNCE_DELAY; c++) {
		HAL_Delay(WAIT_TIME);
		CurrentState = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);
		if (CurrentState != PrevState) {
			c = 0;
			PrevState = CurrentState;
		}
	}
	return CurrentState;
}

